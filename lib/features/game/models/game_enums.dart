// 게임 화면에서 사용되는 열거형 타입을 정의합니다.

/// 크로스워드 각 칸의 시각적 표시 상태.
/// 게임 그리드 위젯에서 칸 배경색과 글자 색상 결정에 사용됩니다.
enum CellDisplayState {
  /// 어떤 단어에도 속하지 않는 칸 (검은 칸, 터치 불가)
  inactive,

  /// 단어에 속하지만 아직 입력이 없는 빈 칸
  empty,

  /// (레거시) 현재 입력 중인 단어 빈 칸 — UI는 테두리로만 선택 표시
  activeWord,

  /// (레거시) 선택 칸 — UI는 [GameController.isSelected] 테두리로 표시
  selected,

  /// 힌트로 미리 오픈된 칸 (정답 글자 표시)
  hint,

  /// 사용자가 입력했지만 해당 단어가 아직 완성되지 않아 정답 판별 전인 칸
  filled,

  /// 사용자 입력이 정답과 일치하는 칸 (단어 전체 완성 후 정답 확인됨)
  correct,

  /// 사용자 입력이 정답과 다른 칸 (단어 전체 완성 후 오답 확인됨)
  incorrect,
}
